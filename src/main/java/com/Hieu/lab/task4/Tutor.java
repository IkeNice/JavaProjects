package com.Hieu.lab.task4;

public class Tutor {

    private int idTutor;
    private String name;

    public Tutor(int idTutor, String name) {
        this.idTutor = idTutor;
        this.name = name;
    }

    public int getIdTutor() {
        return idTutor;
    }

    public String getName() {
        return name;
    }
}
